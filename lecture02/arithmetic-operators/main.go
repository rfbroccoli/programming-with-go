package main

import "fmt"

func main() {
	// Printf format
	// d digit numbers
	// s string text
	num1 := 20
	num2 := 10
	fmt.Printf("num1+num2=%d\n", num1+num2)
	fmt.Printf("num1-num2=%d\n", num1-num2)
	fmt.Printf("1*3=%d\n", 1*3)
	fmt.Print("1/3=", 1/3, "\n")
	fmt.Print("1%3=", 1%3, "\n")
	// fmt.Printf("1+3 = %d\n %s", 1+3, "hello this is another line")
}
